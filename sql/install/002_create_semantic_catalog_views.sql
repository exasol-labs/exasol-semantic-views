  COALESCE(mt.METRIC_KIND, mt.METRIC_TYPE) AS METRIC_KIND,
  mt.METRIC_TYPE,
  be.ENTITY_NAME AS BASE_ENTITY_NAME,
  mt.FORMAT_HINT,
  mt.IS_CERTIFIED,
  mt.IS_PRIVATE,
  mt.OWNER_ROLE,
  mt.DESCRIPTION,
  syn.SYNONYMS,
  sds.DEFINITION_HASH,
  mt.STATUS
FROM SYS_SEMANTIC.METRICS mt
JOIN SYS_SEMANTIC.MODELS m
  ON m.MODEL_ID = mt.MODEL_ID
JOIN SYS_SEMANTIC.MODEL_VERSIONS mv
  ON mv.VERSION_ID = mt.VERSION_ID
LEFT JOIN SYS_SEMANTIC.ENTITIES be
  ON be.ENTITY_ID = mt.BASE_ENTITY_ID
LEFT JOIN SYS_SEMANTIC.OBJECT_COLUMNS oc
  ON oc.COLUMN_KIND = 'METRIC'
 AND oc.OBJECT_REF_ID = mt.METRIC_ID
LEFT JOIN SYS_SEMANTIC.SEMANTIC_OBJECTS so
  ON so.OBJECT_ID = oc.OBJECT_ID
LEFT JOIN (
  SELECT MODEL_ID, VERSION_ID, OBJECT_ID, LISTAGG(SYNONYM, ', ') WITHIN GROUP (ORDER BY SYNONYM) AS SYNONYMS
  FROM SYS_SEMANTIC.SYNONYMS
  WHERE OBJECT_TYPE = 'METRIC'
  GROUP BY MODEL_ID, VERSION_ID, OBJECT_ID
) syn
