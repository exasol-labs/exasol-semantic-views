  mdm.MODEL_ID,
  m.MODEL_NAME,
  mdm.VERSION_ID,
  mv.VERSION_NUMBER,
  so.OBJECT_ID,
  so.OBJECT_NAME,
  mt.METRIC_ID,
  mt.METRIC_NAME,
  d.DIMENSION_ID,
  d.DIMENSION_NAME,
  d.DISPLAY_NAME,
  e.ENTITY_NAME,
  mdm.IS_VALID,
  mdm.REASON_CODE,
  CASE
    WHEN mdm.IS_VALID THEN 'Metric can be grouped or filtered by this dimension.'
    ELSE 'Metric cannot be grouped or filtered by this dimension: ' || mdm.REASON_CODE
  END AS REASON_MESSAGE,
  mdm.RELATIONSHIP_PATH AS JOIN_PATH_NAME,
  mdm.VALIDATION_RUN_ID,
  mdm.UPDATED_AT
FROM SYS_SEMANTIC.METRIC_DIMENSION_MATRIX mdm
JOIN SYS_SEMANTIC.MODELS m
  ON m.MODEL_ID = mdm.MODEL_ID
JOIN SYS_SEMANTIC.MODEL_VERSIONS mv
  ON mv.VERSION_ID = mdm.VERSION_ID
JOIN SYS_SEMANTIC.METRICS mt
  ON mt.METRIC_ID = mdm.METRIC_ID
LEFT JOIN SYS_SEMANTIC.OBJECT_COLUMNS moc
  ON moc.COLUMN_KIND = 'METRIC'
 AND moc.OBJECT_REF_ID = mt.METRIC_ID
LEFT JOIN SYS_SEMANTIC.SEMANTIC_OBJECTS so
  ON so.OBJECT_ID = moc.OBJECT_ID
JOIN SYS_SEMANTIC.DIMENSIONS d
  ON d.DIMENSION_ID = mdm.DIMENSION_ID
JOIN SYS_SEMANTIC.ENTITIES e
  ON e.ENTITY_ID = d.ENTITY_ID;

