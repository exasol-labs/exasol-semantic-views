 AND syn.VERSION_ID = mt.VERSION_ID
 AND syn.OBJECT_ID = mt.METRIC_ID
LEFT JOIN SYS_SEMANTIC.SEMANTIC_DEFINITION_SOURCES sds
  ON sds.DEFINITION_SOURCE_ID = mt.DEFINITION_SOURCE_ID;

CREATE OR REPLACE VIEW SEMANTIC_CATALOG.METRIC_LINEAGE AS
SELECT
  mt.METRIC_ID,
  mt.METRIC_NAME,
  mt.MODEL_ID,
  m.MODEL_NAME,
  mt.VERSION_ID,
  mv.VERSION_NUMBER,
  COALESCE(mi.INPUT_ROLE, md.DEPENDENCY_KIND) AS INPUT_ROLE,
  COALESCE(mi.INPUT_OBJECT_TYPE, md.DEPENDS_ON_OBJECT_TYPE) AS INPUT_OBJECT_TYPE,
  COALESCE(mi.INPUT_OBJECT_ID, md.DEPENDS_ON_OBJECT_ID) AS INPUT_OBJECT_ID,
  COALESCE(f.FACT_NAME, dep_mt.METRIC_NAME, d.DIMENSION_NAME, e.ENTITY_NAME) AS INPUT_OBJECT_NAME,
  mi.ORDINAL_POSITION
FROM SYS_SEMANTIC.METRICS mt
JOIN SYS_SEMANTIC.MODELS m
  ON m.MODEL_ID = mt.MODEL_ID
JOIN SYS_SEMANTIC.MODEL_VERSIONS mv
  ON mv.VERSION_ID = mt.VERSION_ID
LEFT JOIN SYS_SEMANTIC.METRIC_INPUTS mi
  ON mi.METRIC_ID = mt.METRIC_ID
LEFT JOIN SYS_SEMANTIC.METRIC_DEPENDENCIES md
  ON md.METRIC_ID = mt.METRIC_ID
 AND mi.METRIC_ID IS NULL
LEFT JOIN SYS_SEMANTIC.FACTS f
  ON COALESCE(mi.INPUT_OBJECT_TYPE, md.DEPENDS_ON_OBJECT_TYPE) = 'FACT'
 AND f.FACT_ID = COALESCE(mi.INPUT_OBJECT_ID, md.DEPENDS_ON_OBJECT_ID)
LEFT JOIN SYS_SEMANTIC.METRICS dep_mt
  ON COALESCE(mi.INPUT_OBJECT_TYPE, md.DEPENDS_ON_OBJECT_TYPE) = 'METRIC'
 AND dep_mt.METRIC_ID = COALESCE(mi.INPUT_OBJECT_ID, md.DEPENDS_ON_OBJECT_ID)
LEFT JOIN SYS_SEMANTIC.DIMENSIONS d
  ON COALESCE(mi.INPUT_OBJECT_TYPE, md.DEPENDS_ON_OBJECT_TYPE) = 'DIMENSION'
 AND d.DIMENSION_ID = COALESCE(mi.INPUT_OBJECT_ID, md.DEPENDS_ON_OBJECT_ID)
LEFT JOIN SYS_SEMANTIC.ENTITIES e
  ON COALESCE(mi.INPUT_OBJECT_TYPE, md.DEPENDS_ON_OBJECT_TYPE) = 'ENTITY'
 AND e.ENTITY_ID = COALESCE(mi.INPUT_OBJECT_ID, md.DEPENDS_ON_OBJECT_ID)
WHERE COALESCE(mi.INPUT_OBJECT_TYPE, md.DEPENDS_ON_OBJECT_TYPE) IS NOT NULL;

