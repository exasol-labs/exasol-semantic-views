CREATE SCHEMA IF NOT EXISTS MART;

DROP TABLE IF EXISTS MART.ORDER_LINES;
DROP TABLE IF EXISTS MART.ORDERS;
DROP TABLE IF EXISTS MART.CUSTOMERS;
DROP TABLE IF EXISTS MART.PRODUCTS;

CREATE TABLE MART.CUSTOMERS (
  CUSTOMER_ID      DECIMAL(18,0) PRIMARY KEY,
  CUSTOMER_NAME    VARCHAR(200),
  REGION           VARCHAR(100)
);

CREATE TABLE MART.PRODUCTS (
  PRODUCT_ID       DECIMAL(18,0) PRIMARY KEY,
  PRODUCT_NAME     VARCHAR(200),
  CATEGORY         VARCHAR(100)
);

CREATE TABLE MART.ORDERS (
  ORDER_ID         DECIMAL(18,0) PRIMARY KEY,
  CUSTOMER_ID      DECIMAL(18,0) NOT NULL,
  ORDER_DATE       DATE NOT NULL,
  ORDER_STATUS     VARCHAR(32) NOT NULL
);

CREATE TABLE MART.ORDER_LINES (
  ORDER_ID         DECIMAL(18,0) NOT NULL,
  LINE_ID          DECIMAL(18,0) NOT NULL,
  PRODUCT_ID       DECIMAL(18,0) NOT NULL,
  QUANTITY         DECIMAL(18,0) NOT NULL,
  NET_UNIT_PRICE   DECIMAL(18,2) NOT NULL,
  UNIT_COST        DECIMAL(18,2) NOT NULL,
  PRIMARY KEY (ORDER_ID, LINE_ID)
);

